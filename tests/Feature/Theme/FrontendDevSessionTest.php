test('FrontendDevSync merges autodev block into existing .env without wiping custom keys', function () {
    $themePath = frontendDevSyncThemeDir();
    $GLOBALS['__frontendDevSyncThemePath'] = $themePath;

    file_put_contents($themePath . '/frontend.config.php', "<?php\n\nreturn ['stack' => 'vue'];\n");
    file_put_contents($themePath . '/.env', "MY_CUSTOM_KEY=keep-me\nENV_SERVER_SYNC=true\n");

    $config = FrontendConfig::forThemePath($themePath);
    $session = FrontendDevSession::fromOptions('com_demo_app', $config, '127.0.0.1', 8000, 'com_demo_app', true);

    FrontendDevSync::writeAutoDevEnv($themePath, $session, $config, '.env');

    $env = file_get_contents($themePath . '/.env');

    expect($env)->toContain('MY_CUSTOM_KEY=keep-me')
        ->and($env)->toContain('VITE_SERVER_URL=http://127.0.0.1:8000')
        ->and($env)->toContain('VITE_HOT_FILE=dist/hot')
        ->and($env)->toContain('@pinoox-fe-dev end');
});

test('FrontendDevSync wires nested server proxy block without syntax errors', function () {
    $themePath = frontendDevSyncThemeDir();
    $GLOBALS['__frontendDevSyncThemePath'] = $themePath;

    file_put_contents($themePath . '/frontend.config.php', "<?php\n\nreturn ['stack' => 'vue'];\n");
    file_put_contents($themePath . '/vite.config.js', <<<'JS'
import { defineConfig, loadEnv } from 'vite';

export default defineConfig(({ mode }) => {
    const env = loadEnv(mode, process.cwd(), '');

    return {
        plugins: [vue()],
        server: {
            proxy: {
                '/api': env.VITE_SERVER_URL,
            },
        },
    };
});
JS);

    expect(FrontendDevSync::applyViteConfigWiring($themePath))->toBeTrue();

    $config = file_get_contents($themePath . '/vite.config.js');

    expect($config)->toContain('@pinooxhq/vite-plugin')
        ->and($config)->toContain('pinooxHot')
        ->and($config)->toContain('server: pinooxServer(env),')
        ->and($config)->not->toMatch('/pinooxServer\(env\),\s*\n\s*\},/s')
        ->and(substr_count($config, '{'))->toBe(substr_count($config, '}'));
});

