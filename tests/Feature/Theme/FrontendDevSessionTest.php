test('FrontendDevSession builds locked serve URLs at PHP origin without app-router path', function () {
    $themePath = frontendDevSyncThemeDir();
    $GLOBALS['__frontendDevSyncThemePath'] = $themePath;

    file_put_contents($themePath . '/frontend.config.php', "<?php\n\nreturn ['stack' => 'vue'];\n");

    $config = FrontendConfig::forThemePath($themePath);
    $session = FrontendDevSession::fromOptions(
        'com_pinoox_manager',
        $config,
        '127.0.0.1',
        8000,
        'com_pinoox_manager@/manager',
        true,
    );

    expect($session->phpAppUrl)->toBe('http://127.0.0.1:8000')
        ->and($session->displayAppUrls())->toBe(['http://127.0.0.1:8000'])
        ->and($session->proxyPrefixes)->toContain('/api')
        ->and($session->proxyPrefixes)->not->toContain('/manager')
        ->and($session->viteDevServerUrl())->toBe('http://127.0.0.1:5173');
});

test('FrontendDevSync writes autodev env and can wire simple vite config', function () {
    $themePath = frontendDevSyncThemeDir();
    $GLOBALS['__frontendDevSyncThemePath'] = $themePath;

    file_put_contents($themePath . '/frontend.config.php', "<?php\n\nreturn ['stack' => 'vue'];\n");
    file_put_contents($themePath . '/.env', "ENV_SERVER_SYNC=true\n");
    file_put_contents($themePath . '/vite.config.js', <<<'JS'
import { defineConfig, loadEnv } from 'vite';

export default defineConfig(({ mode }) => {
    const env = loadEnv(mode, process.cwd(), '');

    return {
        plugins: [],
        server: {
            proxy: {
                '/api': env.VITE_SERVER_URL,
            },
        },
    };
});
JS);

    $config = FrontendConfig::forThemePath($themePath);
    $session = FrontendDevSession::fromOptions('com_demo_app', $config, '127.0.0.1', 8000, 'com_demo_app', true);
    $corePath = dirname(__DIR__, 3);

    $result = FrontendDevSync::sync($themePath, $config, $corePath, $session, true, '.env');

    expect($result['env_autodev'])->toBeTrue()
        ->and($result['env_file'])->toBe('.env')
        ->and($result['vite_wired'])->toBeTrue()
        ->and(is_file($themePath . '/.env'))->toBeTrue()
        ->and(file_get_contents($themePath . '/.env'))->toContain('@pinoox-fe-dev autogenerated')
        ->and(file_get_contents($themePath . '/vite.config.js'))->toContain('@pinooxhq/vite-plugin')
        ->and(file_get_contents($themePath . '/vite.config.js'))->toContain('pinooxHot')
        ->and(file_get_contents($themePath . '/vite.config.js'))->toContain('pinooxServer');
});

